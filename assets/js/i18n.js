/* ════════════════════════════════════════════════════════
   I18N ENGINE
════════════════════════════════════════════════════════ */

(function () {
  const STORAGE_KEY = 'sgn_lang';
  const DEFAULT_LANG = 'ID';

  // Get saved language or default
  function getCurrentLang() {
    try { return localStorage.getItem(STORAGE_KEY) || DEFAULT_LANG; }
    catch(e) { return DEFAULT_LANG; }
  }

  // Save language preference
  function setLang(lang) {
    try { localStorage.setItem(STORAGE_KEY, lang); }
    catch(e) {}
  }

  // Apply all translations to the page
  function applyTranslations(lang) {
    const t = SGN_I18N[lang];
    if (!t) return;

    // Text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (t[key] !== undefined) {
        el.innerHTML = t[key];
      }
    });

    // Placeholder attributes
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      const key = el.getAttribute('data-i18n-ph');
      if (t[key] !== undefined) el.placeholder = t[key];
    });

    // Update html lang attribute
    const langMap = { ID: 'id', EN: 'en', CN: 'zh' };
    document.documentElement.lang = langMap[lang] || 'id';

    // Update active button state
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
  }

  // Init language switcher buttons
  function initSwitcher() {
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.addEventListener('click', () => {
        const lang = btn.getAttribute('data-lang');
        setLang(lang);
        applyTranslations(lang);
      });
    });
  }

  // Run on DOM ready
  function init() {
    initSwitcher();
    applyTranslations(getCurrentLang());
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }
})();
